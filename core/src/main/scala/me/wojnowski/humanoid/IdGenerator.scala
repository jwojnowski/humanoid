package me.wojnowski.humanoid

trait IdGenerator[F[_], Id] {
  def generate: F[Id]
}

object IdGenerator {
  def apply[F[_], Id](implicit idGenerator: IdGenerator[F, Id]): IdGenerator[F, Id] = idGenerator

  def instance[F[_], Id](f: => F[Id]): IdGenerator[F, Id] = new IdGenerator[F, Id] {
    override def generate: F[Id] = f
  }

}
